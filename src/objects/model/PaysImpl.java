package objects.model;

import enumere.TypeMobilite;
import objects.interfaces.dto.PaysDto;

public class PaysImpl implements PaysDto {
  private String codeIso;
  private String nom;
  private TypeMobilite typeMobilite;
  private int version;


  public int getVersion() {
    return version;
  }

  public void setVersion(int version) {
    this.version = version;
  }

  public String getNom() {
    return nom;
  }

  public void setNom(String nom) {
    this.nom = nom;
  }

  public TypeMobilite getTypeMobilite() {
    return typeMobilite;
  }

  public void setTypeMobilite(TypeMobilite typeMobilite) {
    this.typeMobilite = typeMobilite;
  }

  @Override
  public String getCodeIso() {
    return codeIso;
  }

  @Override
  public void setCodeIso(String code) {
    this.codeIso = code;
  }
}
